'use client';

import { useState } from 'react';
import { useRouter } from 'next/navigation';
import { Button } from '@/components/ui/button';
import { Plus } from 'lucide-react';
import type { Client } from '@/lib/types';
import { CreateClientDialog } from './CreateClientDialog';
import { ClientStatsCards } from './ClientStatsCards';
import { ClientCard } from './ClientCard';
import { ClientDetailDialog } from './ClientDetailDialog';

interface Manager {
  id: string;
  name: string;
  email: string;
  teamName?: string;
}

interface ModernClientsViewProps {
  clients: Client[];
  currentUser: {
    id: string;
    role: string;
    name?: string;
  };
  managers: Manager[];
}

export default function ModernClientsView({
  clients: initialClients,
  currentUser,
  managers,
}: ModernClientsViewProps) {
  const router = useRouter();
  const [selectedClient, setSelectedClient] = useState<Client | null>(null);
  const [createDialogOpen, setCreateDialogOpen] = useState(false);

  const handleClientUpdated = () => {
    router.refresh();
  };

  const handleCloseDialog = () => {
    setSelectedClient(null);
  };

  return (
    <div className="space-y-6">
      {/* Header */}
      <div className="flex items-center justify-between">
        <div>
          <h2 className="text-3xl font-bold tracking-tight">Client Portfolio</h2>
          <p className="text-muted-foreground mt-1">
            Manage and grow your client relationships
          </p>
        </div>
        {['CEO', 'ADMIN', 'MANAGER'].includes(currentUser.role) && (
          <Button onClick={() => setCreateDialogOpen(true)} className="gap-2">
            <Plus className="h-4 w-4" />
            Add Client
          </Button>
        )}
      </div>

      {/* Create Client Dialog */}
      <CreateClientDialog
        open={createDialogOpen}
        onOpenChange={setCreateDialogOpen}
        currentUser={currentUser}
        managers={managers}
      />

      {/* Stats Cards */}
      <ClientStatsCards clients={initialClients} />

      {/* Client Grid */}
      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
        {initialClients.map((client) => (
          <ClientCard
            key={client.id}
            client={client}
            onClick={() => setSelectedClient(client)}
          />
        ))}
      </div>

      {/* Client Detail Dialog */}
      <ClientDetailDialog
        client={selectedClient}
        open={!!selectedClient}
        onClose={handleCloseDialog}
        currentUserId={currentUser.id}
        accountManagers={managers}
        onClientUpdated={handleClientUpdated}
      />
    </div>
  );
}
